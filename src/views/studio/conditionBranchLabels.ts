import type { Branch } from './types';

const AUTO_GENERATED_CONDITION_LABEL_PATTERN = /^Condition(?: \d+)?(?: \(Copy(?: \d+)?\))?$/i;
export const CONDITION_NAME_FALLBACK = 'Condition';
const AUTO_GENERATED_FALLBACK_PATH_LABEL = /^fallback$/i;

export const CONDITION_QUESTION_PLACEHOLDER = 'Edit condition name';

export const isAutoGeneratedConditionLabel = (label?: string): boolean => {
    const trimmedLabel = label?.trim() || '';
    if (!trimmedLabel) {
        return false;
    }

    return AUTO_GENERATED_CONDITION_LABEL_PATTERN.test(trimmedLabel);
};

export const getConditionQuestionLabel = (question?: string, legacyLabel?: string): string => {
    // Treat an explicit empty string as "intentionally blank" so the old label
    // does not keep resurfacing after a creator has split name vs question.
    if (question !== undefined) {
        return question.trim();
    }

    const trimmedLabel = legacyLabel?.trim() || '';
    if (!trimmedLabel) {
        return '';
    }

    if (isAutoGeneratedConditionLabel(trimmedLabel)) {
        return '';
    }

    return trimmedLabel;
};

export const materializeConditionQuestion = (question?: string, legacyLabel?: string): string => {
    if (question !== undefined) {
        return question;
    }

    return getConditionQuestionLabel(undefined, legacyLabel);
};

export const getConditionPathLabel = (
    branch: Pick<Branch, 'condition' | 'isDefault' | 'logic'>
): string => {
    const trimmedCondition = branch.condition?.trim();
    if (trimmedCondition && !(branch.isDefault && AUTO_GENERATED_FALLBACK_PATH_LABEL.test(trimmedCondition))) {
        return trimmedCondition;
    }
    if (!branch.isDefault) {
        const trimmedValue = branch.logic?.value !== undefined ? String(branch.logic.value).trim() : '';
        if (trimmedValue) return trimmedValue;
    }
    return 'Path';
};

export const getConditionRuleSummary = (
    branch: Pick<Branch, 'isDefault' | 'logic'>,
    variableFallback?: string
): string | null => {
    if (branch.isDefault) {
        return 'Use when none of the others match';
    }

    const variable = branch.logic?.variable?.trim() || variableFallback?.trim() || '';
    if (!variable) {
        return null;
    }

    const value = branch.logic?.value !== undefined ? String(branch.logic.value).trim() : '';
    return `${variable} is ${value || 'value'}`;
};

export const getConditionSelectionLabel = (
    branch: Pick<Branch, 'condition' | 'isDefault' | 'logic'>,
    variableFallback?: string
): string => {
    const pathLabel = getConditionPathLabel(branch);
    const ruleSummary = getConditionRuleSummary(branch, variableFallback);
    return ruleSummary ? `${pathLabel} (${ruleSummary})` : pathLabel;
};
